                    the internet-based test.
                </li>
                <li>
                    TOEFL <strong> PBT </strong> is offered six times a year in areas where Internet-based
                    testing is not available.
                </li>
            </ul>

        </p>

        <p>
            The TOEFL PBT Test is a paper-based test that measures your ability to use and understand
            English in a classroom setting at the college or university level.
            It accurately measures how well students can listen, read and write English while performing
            academic tasks.
        </p>

        <p>
            The TOEFL iBT™ test measures your ability to use and understand English at the university level.
